# 整数
print(2 + 3)
print(3 - 2)
print(2 * 3)
print(3 / 2)

# べき乗
print(3 ** 2)
print(3 ** 3)
print(10 ** 6)

# 複数の演算子
print(2 + 3*4)
print((2 + 3) * 4)

# 浮動小数点数
print(0.2 + 0.1)
print(3 * 0.1)

# 整数と浮動小数点数
print(4 / 2)
print(1 + 2.0)
print(2 * 3.0)
print(3.0 ** 2)

# アンダースコア
universe_age = 14_000_000_000
print(universe_age)

# 複数同時の代入
x, y, z = 0, 0, 0
print(x)
print(y)
print(z)

# 定数
MAX_CONNECTION = 5000
